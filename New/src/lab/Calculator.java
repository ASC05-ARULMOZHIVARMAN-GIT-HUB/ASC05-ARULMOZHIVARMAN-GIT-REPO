package lab;

public class Calculator {

    // 1. Method for two integers
    public int add(int a, int b) {
        return a + b;
    }

    // 2. Method for three integers
    public int add(int a, int b, int c) {
        return a + b + c;
    }

    // 3. Method for two doubles
    public double add(double a, double b) {
        return a + b;
    }

    // 4. Method for String and int
    public String add(String a, int b) {
        return a + " " + b;
}}